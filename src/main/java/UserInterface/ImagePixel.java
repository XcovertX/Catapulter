package main.java.UserInterface;

public class ImagePixel {
	
	private int rgb;
	private int roughness;
	
	public ImagePixel() {
		
		
	}

	public int getRgb() {
		
		return rgb;
	}

	public void setRgb( int rgb ) {
		
		this.rgb = rgb;
	}

	public int getRoughness() {
		
		return roughness;
	}

	public void setRoughness( int roughness ) {
		
		this.roughness = roughness;
	}

}
